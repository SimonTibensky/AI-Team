function renderList(containerId, items, template) {
  const container = document.getElementById(containerId);
  if (!container) return;
  container.innerHTML = items.map((item, index) => template(item, index)).join("");
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function renderTags(tags, className) {
  return `
    <div class="${className}">
      ${tags.map((tag) => `<span>${escapeHtml(tag)}</span>`).join("")}
    </div>
  `;
}

function getInitials(name) {
  return name
    .split(" ")
    .map((part) => part[0])
    .join("")
    .slice(0, 2)
    .toUpperCase();
}

function renderTeamPhoto(member) {
  if (member.image) {
    return `
      <div class="team-photo">
        <img src="${escapeHtml(member.image)}" alt="${escapeHtml(member.name)}" loading="lazy">
      </div>
    `;
  }

  return `
    <div class="team-photo team-photo--placeholder" aria-label="Miesto pre fotografiu člena">
      <div class="team-placeholder">${escapeHtml(getInitials(member.name))}</div>
    </div>
  `;
}

function renderContacts(contacts) {
  return `
    <div class="team-contacts">
      ${contacts.map((contact) => `
        <div class="team-contact">
          <span>${escapeHtml(contact.label)}</span>
          ${
            contact.href
              ? `<a href="${escapeHtml(contact.href)}">${escapeHtml(contact.value)}</a>`
              : `<strong>${escapeHtml(contact.value)}</strong>`
          }
        </div>
      `).join("")}
    </div>
  `;
}

